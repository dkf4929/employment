package com.project.employment.exception;

public class FileExtensionException extends CommonException {
    public FileExtensionException() {
        super();
    }

    public FileExtensionException(String message) {
        super(message);
    }

    public FileExtensionException(String message, Throwable cause) {
        super(message, cause);
    }

    public FileExtensionException(Throwable cause) {
        super(cause);
    }
}
